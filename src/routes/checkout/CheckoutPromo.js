import React from "react";

export default class CheckoutPromo extends React.Component {
  render() {
    const { togglePromocode, promoCode } = this.props;
    return (
      <section className="promo-code">
        <p onClick={togglePromocode}>I have a promo code!</p>
        {promoCode === false ? null : (
          <input
            className=" pop-font"
            type="text"
            placeholder="enter the code"
          />
        )}
      </section>
    );
  }
}
